def final_cleanup():
    print("Finalizing data organization and merging folders...")

    for split in ['train', 'val', 'test']:
        split_path = os.path.join(OUTPUT_PATH, split)
        if not os.path.exists(split_path): continue

        # 1. Create a list of lowercase folder names to merge
        for folder in os.listdir(split_path):
            old_folder_path = os.path.join(split_path, folder)
            if not os.path.isdir(old_folder_path): continue

            target_name = folder.lower().strip()
            target_path = os.path.join(split_path, target_name)

            # If the current folder is Capitalized (like Emergency), move its files to lowercase (emergency)
            if folder != target_name:
                os.makedirs(target_path, exist_ok=True)
                for file in os.listdir(old_folder_path):
                    shutil.move(os.path.join(old_folder_path, file), os.path.join(target_path, file))
                os.rmdir(old_folder_path)
                print(f"Merged {folder} into {target_name}")

        # 2. Rename files inside each merged folder to be uniform (label_1, label_2...)
        for label in os.listdir(split_path):
            label_path = os.path.join(split_path, label)
            if not os.path.isdir(label_path): continue

            # Sort files to maintain order
            files = sorted([f for f in os.listdir(label_path)])
            for index, filename in enumerate(files, start=1):
                old_file = os.path.join(label_path, filename)
                new_file = os.path.join(label_path, f"{label}_{index}.mp4")

                # Use a temporary name to avoid "file already exists" errors during renaming
                temp_file = os.path.join(label_path, f"temp_{index}.mp4")
                os.rename(old_file, temp_file)
                os.rename(temp_file, new_file)

    print("Success! Everything is merged and renamed correctly.")

final_cleanup()

