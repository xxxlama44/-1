def trim_video(input_path, output_path):

    cap = cv2.VideoCapture(input_path)

    if not cap.isOpened():
        shutil.copy(input_path, output_path)
        return "copied (error)"

    fps = cap.get(cv2.CAP_PROP_FPS)
    if fps <= 0:
        fps = 30

    w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    frames = []
    motion = []

    ret, prev = cap.read()
    if not ret:
        cap.release()
        return "empty"

    prev_gray = cv2.cvtColor(prev, cv2.COLOR_BGR2GRAY)
    frames.append(prev)

    while True:
        ret, frame = cap.read()
        if not ret:
            break

        gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
        diff = np.mean(cv2.absdiff(prev_gray, gray))

        motion.append(diff)
        frames.append(frame)
        prev_gray = gray

    cap.release()

    motion = np.array(motion)

    active = np.where(motion > 0.3)[0]

    if len(active) == 0:
        shutil.copy(input_path, output_path)
        return "no motion → copied"

    start = max(0, active[0] - 10)
    end = min(len(frames), active[-1] + 10)

    out = cv2.VideoWriter(
        output_path,
        cv2.VideoWriter_fourcc(*'mp4v'),
        fps,
        (w, h)
    )

    for i in range(start, end):
        out.write(frames[i])

    out.release()

    return "trimmed"


